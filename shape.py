#!/usr/bin/env python
import turtle

def draw_square():
    window =turtle.Screen()
    window.bgcolor("red")
    
    brad = turtle.Turtle()
    brad.forward(120)
    brad.right(90)
    brad.forward(120)
    brad.right(90)
    brad.forward(120)
    brad.right(90)
    brad.forward(120)
    brad.right(90)
    brad.forward(120)
    brad.right(90)






draw_square()
